package com.docotel.ki.pojo;

public class LapDataFormalitas {

	private Integer no;
	private String applicationNo;
	private String fillingDate;
	private String kelas;
	private String brandType;
	private String brandName;
	private String brmNo;
	private String jenisPermohonan;
	
	public Integer getNo() {
		return no;
	}
	public void setNo(Integer no) {
		this.no = no;
	}
	public String getApplicationNo() {
		return applicationNo;
	}
	public void setApplicationNo(String applicationNo) {
		this.applicationNo = applicationNo;
	}
	public String getFillingDate() {
		return fillingDate;
	}
	public void setFillingDate(String fillingDate) {
		this.fillingDate = fillingDate;
	}
	public String getKelas() {
		return kelas;
	}
	public void setKelas(String kelas) {
		this.kelas = kelas;
	}
	public String getBrandType() {
		return brandType;
	}
	public void setBrandType(String brandType) {
		this.brandType = brandType;
	}
	public String getBrandName() {
		return brandName;
	}
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	public String getBrmNo() {
		return brmNo;
	}
	public void setBrmNo(String brmNo) {
		this.brmNo = brmNo;
	}
	public String getJenisPermohonan() {
		return jenisPermohonan;
	}
	public void setJenisPermohonan(String jenisPermohonan) {
		this.jenisPermohonan = jenisPermohonan;
	}
	
	
}
