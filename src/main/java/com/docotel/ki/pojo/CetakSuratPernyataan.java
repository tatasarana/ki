package com.docotel.ki.pojo;

public class CetakSuratPernyataan {
	private String brandName;
	private String brandLogo;
	private String ownerName;
	private String ownerAddress;
	private String createdDate;
	private String ownerOrReprsName;
	
	public String getBrandName() {
		return brandName;
	}
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	public String getBrandLogo() {
		return brandLogo;
	}
	public void setBrandLogo(String brandLogo) {
		this.brandLogo = brandLogo;
	}
	public String getOwnerName() {
		return ownerName;
	}
	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}
	public String getOwnerAddress() {
		return ownerAddress;
	}
	public void setOwnerAddress(String ownerAddress) {
		this.ownerAddress = ownerAddress;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	public String getOwnerOrReprsName() {
		return ownerOrReprsName;
	}
	public void setOwnerOrReprsName(String ownerOrReprsName) {
		this.ownerOrReprsName = ownerOrReprsName;
	}
	
}
